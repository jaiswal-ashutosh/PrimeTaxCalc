// Utility functions
function formatNumber(num) {
    return new Intl.NumberFormat('en-IN').format(Math.round(num));
}

function debounce(func, wait) {
    let timeout;
    return function executedFunction(...args) {
        const later = () => {
            clearTimeout(timeout);
            func(...args);
        };
        clearTimeout(timeout);
        timeout = setTimeout(later, wait);
    };
}

